public class Sol7_alternate {

	public static void main(String[] args)
	{
		
			for (int i = 0; i < 4; i++)
			{
				for (int j = 0; j <=i; j++)
				{
					System.out.print("*");
				}
				
				System.out.println();
			}
			for (int i = 5;  i<=1; i--)
			{
				for (int l = 1; l <=i; l++)
				{
					System.out.print("*");
				}
				System.out.println();
			}
	}

}
